"""购物 Agent 工具集。

通过 LLM 的内置知识直接生成商品推荐列表（含购买链接）。
"""


def search_products(query: str) -> str:
    """根据用户需求搜索并推荐商品。

    此工具依赖你（LLM）的训练数据来生成推荐。你需要为用户的查询推荐 3~5 款匹配的商品，
    每款商品包含以下信息：
      - name: 商品名称
      - price: 价格（人民币，如 "¥199"）
      - description: 简短描述
      - link: 京东或天猫的搜索链接（格式: https://search.jd.com/Search?keyword=商品名）
      - rating: 好评率（如 "97% 好评"）

    重要要求：
    - 价格必须切合实际（2025~2026 年中国市场参考价）
    - 优先推荐知名品牌的热门型号
    - 如果用户有预算限制，严格控制在预算以内
    - 购买链接必须是可以直接点击打开的完整京东/天猫搜索 URL

    Args:
        query: 用户的商品需求描述，如 "200元以下的足球鞋"、"适合学生的笔记本电脑"。

    Returns:
        将此 prompt 返回后，由 LLM 根据训练数据生成具体的商品推荐 JSON 列表。
    """
    return (
        f"请根据以下用户需求推荐商品：{query}\n\n"
        "要求：推荐 3~5 款匹配的商品。请用 JSON 数组格式输出，每项包含：\n"
        '  name: 商品名称\n'
        '  price: 参考价格（人民币，字符串格式如 "¥199"）\n'
        '  description: 简短描述\n'
        '  link: 京东搜索链接（格式: https://search.jd.com/Search?keyword=商品名称）\n'
        '  rating: 好评率\n\n'
        "价格需切合2025~2026年中国市场实际水平。优先推荐知名品牌热门款。"
        "如用户有预算限制，严格按预算推荐。"
    )
